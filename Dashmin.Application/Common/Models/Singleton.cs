using System;

namespace Dashmin.Application.Common.Models
{

    public class Singleton
    {
        private static Singleton instance;

        private Singleton() { }

        public static Singleton Instance
        {
            get
            {
                if (instance == null)
                {
                    instance = new Singleton();
                }
                return instance;
            }
        }

        /// non-static members
        public int Foo { get; set; }

    }
}